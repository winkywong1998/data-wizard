package table.relation;

public class Help {
    private int vid;
    private  int mid;

    public Help(int vid, int mid) {
        this.vid = vid;
        this.mid = mid;
    }

    public int getVid() {
        return vid;
    }

    public void setVid(int vid) {
        this.vid = vid;
    }

    public int getMid() {
        return mid;
    }

    public void setMid(int mid) {
        this.mid = mid;
    }
}
