package table.relation;

public class Hold {
    private int mid;
    private int aid;

    public Hold(int mid, int aid) {
        this.mid = mid;
        this.aid = aid;
    }

    public int getMid() {
        return mid;
    }

    public void setMid(int mid) {
        this.mid = mid;
    }

    public int getAid() {
        return aid;
    }

    public void setAid(int aid) {
        this.aid = aid;
    }
}
